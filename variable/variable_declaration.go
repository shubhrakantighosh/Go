package main

import "fmt"

func main() {

	//Integer
	var integer1 int = 1
	var integer2 int
	integer2 = 8
	integer3 := 9

	fmt.Println(integer1, integer2, integer3)

	//Float

	var float1 float64 = 2.78
	var float2 float64
	float2 = 3.4
	float3 := 5.987

	fmt.Println(float1, float2, float3)

	//String
	var str1 string = "Hi"
	var str2 string
	str2 = "Hi"
	str3 := "Hi"

	fmt.Println(str1, str2, str3)

	//Boolean
	var bool1 bool = true
	var bool2 bool
	bool2 = false
	bool3 := true

	fmt.Println(bool1, bool2, bool3)

}
